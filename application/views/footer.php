		<li><p align="justify">Tahun 1619, Pemerintahan Belanda (VOC) di bawah kepemimpinan Jan PieterszoonCoen menghancurkan Jayakarta dan dengan serta merta membangun kota baru yang terletak di bagian barat sungai Ciliwung, yang dia namakan Batavia, nama yang diambil dari Batavieren, nenek moyang bangsa Belanda</li>
		<li><p align="justify">Batavia direncanakan dan dibangun nyaris mirip dengan kota-kota di Belanda, yaitu dibangun dalam bentuk blok, masing-masih dipisahkan oleh kanal dan dilindungi oleh dinding sebagai benteng, dan parit. Batavia ini selesai dibangun pada 1650. Batavia tua adalah tempat tinggal bangsa Eropa, sementara bangsa Cina, Jawa dan penduduk asli lainnnya disingkirkan ke tempat lainnya.</li>
		<li><p align="justify">Di masa-masa kejayaannya Batavia yang terkenal sebagai ‘Permata dari timur’, diduduki oleh VOC dan kemudain akhirnya diduduki pemerintah Belanda yang terbentang luas di kepulauan Hindia timur.</li>
		<li><p align="justify">Kemudian pada masa penjajahan Jepang di tahun 1942, nama Batavia diganti menjadi Jakarta.</li>
