    <li><p align="justify">Area yang terbilang paling tua di Jakarta adalah bagian utara pantai barat Jawa dimana sungai Ciliwung berada, mengailiri teluk-teluk di Jakarta.</li>
    <li><p align="justify">Kota pelabuhan ini pada mulanya bernama Sunda Kelapa, namun pada 22 Juni 1527 Pangeran Fatahillah menghancurkan Sunda Kelapa dan sebagai gantinya mendirikan kota Jayakarta di area tersebut. Tanggal inilah yang kemudian ditetapkan sebagai tanggal berdirinya kota Jakarta.</li>
